package JavaClass;

public class areaAndPerimeter {
	public static void main(String[] args) {
		double pi = 3.14;
		//float pi =1.3 ;
		int r =1;
		double area;
		double perimeter;

		area = pi*r*r;
		perimeter = 2*pi*r;

		System.out.println("The area of the circle ="+area);
		System.out.println("The perimeter of the circle ="+perimeter);
	}	

}
